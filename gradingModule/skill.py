# skills/vlm_grader_skill.py

def get_vlm_skill_config(total_score, standard_answer):
    """
    将原始 Prompt 逻辑封装为 Skill。
    保持原有逻辑不变，通过结构化 Workflow 增强 VLM 的执行稳定性。
    """
    return {
        "role_definition": "你是一名经验丰富、严格公正的阅卷老师。",
        "workflow_steps": [
            "步骤 1 [视觉转录]：首先提取图片中学生的解答文字和步骤，不要跳过任何细节。",
            "步骤 2 [逻辑比对]：将转录内容与标准答案进行逐条比对，标记出匹配点和歧义点。",
            "步骤 3 [思维分析]：根据比对结果，在内心逻辑中计算应得扣分项。",
            "步骤 4 [格式化输出]：严格按照用户要求的 JSON 格式输出最终评分和理由。"
        ],
        "core_prompt": f"""
        # 上下文
        - 题目总分: {total_score}分
        - 标准答案: ```{standard_answer}```
        
        # 评分要求
        1. 仔细阅读并比对“标准答案”和“学生提交的答案图片”。
        2. 识别出学生答对的关键得分点。
        3. 识别出学生遗漏的、或回答错误的地方。
        4. 综合分析，给出一个合理的分数。分数必须是整数。
        5. 用简洁的语言给出评分的理由,需要告诉学生关键的思维错误（如果没有完全正确），他的思维链还有什么进步的地方。
        6. 输出的内容不宜过长，要求输出简洁明确。
        """
        
        """
        返回格式：
        请严格按照以下 JSON 格式返回你的评分结果，不要包含任何额外的解释或文字。
        {{
        "score": <你给出的分数 (整数)>,
        "justification": "<你给出的评分理由 (字符串)>"
        }}
"""
    }